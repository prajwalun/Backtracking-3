# The solveNQueens method returns all valid configurations for the N-Queens problem.

# Use backtracking with sets to track columns and diagonals.
# Place queens row by row, ensuring no conflicts.
# Backtrack by removing queens and updating tracking sets.

# TC: O(n!) - Permutations of queen placements.
# SC: O(n) - Space for sets and recursion stack.



from typing import List


class Solution:
    def solveNQueens(self, n: int) -> List[List[str]]:
        col = set()
        posDiag = set()
        negDiag = set()

        res = []
        board = [["."] * n for i in range(n)]

        def backtrack(r):
            if r == n:
                copy = ["".join(row) for row in board]
                res.append(copy)
                return

            for c in range(n):
                if c in col or (r + c) in posDiag or (r - c) in negDiag:
                    continue

                col.add(c)
                posDiag.add(r + c)
                negDiag.add(r - c)
                board[r][c] = "Q"

                backtrack(r + 1)

                col.remove(c)
                posDiag.remove(r + c)
                negDiag.remove(r - c)
                board[r][c] = "."

        backtrack(0)
        return res